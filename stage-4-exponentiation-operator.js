// x ** y  = y powers of x
let squared = 2 ** 2; // same as: 2 * 2
console.log(squared);

let cubed = 2 ** 3; // same as: 2 * 2 * 2
console.log(cubed);


// x **= y   x = y powers of x
let a = 2;
a **= 2; // same as: a = a * a;
console.log(a);

let b = 3;
b **= 3; // same as: b = b * b * b;
console.log(b);